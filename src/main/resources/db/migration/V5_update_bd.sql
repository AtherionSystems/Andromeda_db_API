-- ============================================================
-- TABLE: CAPABILITIES
-- ============================================================
CREATE TABLE capabilities (
                              id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                              project_id  NUMBER        NOT NULL,
                              name        VARCHAR2(255) NOT NULL,
                              description CLOB,
                              status      VARCHAR2(20)  DEFAULT 'active'
                                  CHECK (status IN ('active', 'completed', 'cancelled')),
                              created_by  NUMBER,
                              created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                              updated_by  NUMBER,
                              updated_at  TIMESTAMP,
                              CONSTRAINT fk_capabilities_project    FOREIGN KEY (project_id) REFERENCES projects (id),
                              CONSTRAINT fk_capabilities_created_by FOREIGN KEY (created_by) REFERENCES users (id),
                              CONSTRAINT fk_capabilities_updated_by FOREIGN KEY (updated_by) REFERENCES users (id),
                              CONSTRAINT chk_cap_dates              CHECK (updated_at IS NULL OR updated_at >= created_at)
);

CREATE INDEX idx_capabilities_project_id ON capabilities (project_id);

-- ============================================================
-- TABLE: FEATURES
-- ============================================================
CREATE TABLE features (
                          id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          capability_id NUMBER        NOT NULL,
                          name          VARCHAR2(255) NOT NULL,
                          description   CLOB,
                          status        VARCHAR2(20)  DEFAULT 'active'
                              CHECK (status IN ('active', 'completed', 'cancelled')),
                          created_by    NUMBER,
                          created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                          updated_by    NUMBER,
                          updated_at    TIMESTAMP,
                          CONSTRAINT fk_features_capability FOREIGN KEY (capability_id) REFERENCES capabilities (id),
                          CONSTRAINT fk_features_created_by FOREIGN KEY (created_by)    REFERENCES users (id),
                          CONSTRAINT fk_features_updated_by FOREIGN KEY (updated_by)    REFERENCES users (id),
                          CONSTRAINT chk_feat_dates         CHECK (updated_at IS NULL OR updated_at >= created_at)
);

CREATE INDEX idx_features_capability_id ON features (capability_id);

-- ============================================================
-- TABLE: USER STORIES
-- ============================================================
CREATE TABLE user_stories (
                              id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                              feature_id          NUMBER        NOT NULL,
                              title               VARCHAR2(255) NOT NULL,
                              description         CLOB,
                              acceptance_criteria CLOB,
                              priority            VARCHAR2(10)  DEFAULT 'medium'
                                  CHECK (priority IN ('low', 'medium', 'high', 'critical')),
                              status              VARCHAR2(20)  DEFAULT 'todo'
                                  CHECK (status IN ('todo', 'in_progress', 'review', 'done')),
                              story_points        NUMBER(3),
                              owner_id            NUMBER,
                              created_by          NUMBER        NOT NULL,
                              created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                              updated_by          NUMBER,
                              updated_at          TIMESTAMP,
                              CONSTRAINT fk_us_feature    FOREIGN KEY (feature_id) REFERENCES features (id),
                              CONSTRAINT fk_us_owner      FOREIGN KEY (owner_id)   REFERENCES users (id),
                              CONSTRAINT fk_us_created_by FOREIGN KEY (created_by) REFERENCES users (id),
                              CONSTRAINT fk_us_updated_by FOREIGN KEY (updated_by) REFERENCES users (id),
                              CONSTRAINT chk_us_dates     CHECK (updated_at IS NULL OR updated_at >= created_at)
);

CREATE INDEX idx_us_feature_id ON user_stories (feature_id);

-- ============================================================
-- TABLE: USER STORY DEPENDENCIES
-- ============================================================
CREATE TABLE user_story_dependencies (
                                         id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                         story_id        NUMBER       NOT NULL,
                                         blocked_by_id   NUMBER       NOT NULL,
                                         dependency_type VARCHAR2(20) DEFAULT 'blocks' NOT NULL
                                             CHECK (dependency_type IN (
                                                                        'blocks',
                                                                        'related',
                                                                        'duplicates',
                                                                        'split_from',
                                                                        'parent_child'
                                                 )),
                                         created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                                         CONSTRAINT fk_usd_story      FOREIGN KEY (story_id)      REFERENCES user_stories (id),
                                         CONSTRAINT fk_usd_blocked_by FOREIGN KEY (blocked_by_id) REFERENCES user_stories (id),
                                         CONSTRAINT uq_usd_pair       UNIQUE (story_id, blocked_by_id),
                                         CONSTRAINT chk_usd_no_self   CHECK  (story_id != blocked_by_id)
);

CREATE INDEX idx_usd_story_id      ON user_story_dependencies (story_id);
CREATE INDEX idx_usd_blocked_by_id ON user_story_dependencies (blocked_by_id);

-- ============================================================
-- TABLE: SPRINT STORIES
-- ============================================================
CREATE TABLE sprint_stories_assignments (
                                            id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                            sprint_id     NUMBER    NOT NULL,
                                            user_story_id NUMBER    NOT NULL,
                                            added_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                                            removed_at    TIMESTAMP,
                                            moved_to      NUMBER,
                                            is_active     NUMBER(1) DEFAULT 1 NOT NULL
                                                CHECK (is_active IN (0, 1)),
                                            CONSTRAINT fk_ss_sprint FOREIGN KEY (sprint_id)     REFERENCES sprints (id),
                                            CONSTRAINT fk_ss_story  FOREIGN KEY (user_story_id) REFERENCES user_stories (id),
                                            CONSTRAINT fk_ss_moved  FOREIGN KEY (moved_to)      REFERENCES sprints (id)
);

CREATE UNIQUE INDEX uq_ss_one_active
    ON sprint_stories_assignments (
                                   CASE WHEN is_active = 1 THEN sprint_id     ELSE NULL END,
                                   CASE WHEN is_active = 1 THEN user_story_id ELSE NULL END
        );

CREATE INDEX idx_ss_sprint_id ON sprint_stories_assignments (sprint_id);
CREATE INDEX idx_ss_story_id  ON sprint_stories_assignments (user_story_id);

