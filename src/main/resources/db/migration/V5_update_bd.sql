-- ============================================================
-- TABLE: STORY SPILLOVERS
-- ============================================================
CREATE TABLE story_spillovers (
                                  id                    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                  sprint_story_id       NUMBER        NOT NULL,
                                  user_story_id         NUMBER        NOT NULL,
                                  origin_sprint_id      NUMBER        NOT NULL,
                                  destination_sprint_id NUMBER        NOT NULL,
                                  reason                VARCHAR2(50)  NOT NULL
                                      CHECK (reason IN (
                                                        'scope_change',
                                                        'blocked',
                                                        'underestimated',
                                                        'resource_unavailable',
                                                        'technical_issue',
                                                        'other'
                                          )),
                                  detail                VARCHAR2(1000),
                                  created_by            NUMBER        NOT NULL,
                                  created_at            TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                                  updated_by            NUMBER,
                                  updated_at            TIMESTAMP,
                                  CONSTRAINT fk_spill_sprint_story  FOREIGN KEY (sprint_story_id)       REFERENCES sprint_stories_assignments (id),
                                  CONSTRAINT fk_spill_story         FOREIGN KEY (user_story_id)         REFERENCES user_stories (id),
                                  CONSTRAINT fk_spill_origin        FOREIGN KEY (origin_sprint_id)      REFERENCES sprints (id),
                                  CONSTRAINT fk_spill_destination   FOREIGN KEY (destination_sprint_id) REFERENCES sprints (id),
                                  CONSTRAINT fk_spill_created       FOREIGN KEY (created_by)            REFERENCES users (id),
                                  CONSTRAINT fk_spill_updated       FOREIGN KEY (updated_by)            REFERENCES users (id),
                                  CONSTRAINT chk_spill_dates        CHECK (updated_at IS NULL OR updated_at >= created_at)
);

CREATE INDEX idx_spill_story_id ON story_spillovers (user_story_id);
CREATE INDEX idx_spill_origin   ON story_spillovers (origin_sprint_id);

-- ============================================================
-- TABLE: TECHNICAL DEBT
-- ============================================================
CREATE TABLE technical_debt (
                                id            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                project_id    NUMBER        NOT NULL,
                                user_story_id NUMBER,
                                task_id       NUMBER,
                                title         VARCHAR2(255) NOT NULL,
                                description   CLOB,
                                debt_type     VARCHAR2(30)  NOT NULL
                                    CHECK (debt_type IN (
                                                         'code_quality',
                                                         'missing_tests',
                                                         'security',
                                                         'performance',
                                                         'documentation',
                                                         'architecture'
                                        )),
                                priority      VARCHAR2(10)  DEFAULT 'medium'
                                    CHECK (priority IN ('low', 'medium', 'high', 'critical')),
                                status        VARCHAR2(20)  DEFAULT 'open'
                                    CHECK (status IN ('open', 'in_progress', 'resolved', 'accepted')),
                                assigned_to   NUMBER        NOT NULL,
                                resolved_at   TIMESTAMP,
                                created_by    NUMBER        NOT NULL,
                                created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                                updated_by    NUMBER,
                                updated_at    TIMESTAMP,
                                CONSTRAINT fk_debt_project  FOREIGN KEY (project_id)    REFERENCES projects (id),
                                CONSTRAINT fk_debt_story    FOREIGN KEY (user_story_id) REFERENCES user_stories (id),
                                CONSTRAINT fk_debt_task     FOREIGN KEY (task_id)       REFERENCES tasks (id),
                                CONSTRAINT fk_debt_assigned FOREIGN KEY (assigned_to)   REFERENCES users (id),
                                CONSTRAINT fk_debt_created  FOREIGN KEY (created_by)    REFERENCES users (id),
                                CONSTRAINT fk_debt_updated  FOREIGN KEY (updated_by)    REFERENCES users (id),
                                CONSTRAINT chk_debt_dates   CHECK (updated_at IS NULL OR updated_at >= created_at)
);

CREATE INDEX idx_debt_project_id ON technical_debt (project_id);
CREATE INDEX idx_debt_story_id   ON technical_debt (user_story_id);
CREATE INDEX idx_debt_status     ON technical_debt (status);